package model.entity;

/**
 *
 * @author toden
 */
public class MenuLocation {
    public String name;
    public String image;

    public MenuLocation(String name, String image) {
        this.name = name;
        this.image = image;
    }
    
    public MenuLocation(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
    
    
}
